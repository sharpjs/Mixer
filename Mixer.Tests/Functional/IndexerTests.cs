namespace Mixer.Tests.Functional;

[TestFixture]
public class IndexerTests
{
    [Test]
    public void Simple()
    {
        new FunctionalTestBuilder()
        .WithInput(
            """
            using Mixer;

            namespace Test;

            class Thing { }

            [Mixin]
            $source Source
            {
                public Thing this[int index] => new();
            }

            [Include<Source>]
            partial $target Target { }
            """
        )
        .ExpectGeneratedSource(
            "Test.Target.1.g.cs",
            """
            // <auto-generated>
            //   This code file was generated by the 'Mixer' NuGet package.
            //   See https://github.com/sharpjs/Mixer for more information.
            // </auto-generated>

            namespace Test;

            #region Source
            #nullable enable

            partial $target Target
            {
                [global::System.CodeDom.Compiler.GeneratedCodeAttribute("Mixer.Generator", "0.0.0.0")]
                public global::Test.Thing this[int index] => new();
            }

            #nullable restore
            #endregion
            """
        )
        .Test();
    }

    [Test]
    public void ExplicitInterfaceImplementation()
    {
        new FunctionalTestBuilder()
        .WithInput(
            """
            using Mixer;

            namespace Test;

            class Thing { }
            interface IHasThing { Thing this[int index] { get; } }

            [Mixin]
            $source Source : IHasThing
            {
                Thing IHasThing.this[int index] => new();
            }

            [Include<Source>]
            partial $target Target { }
            """
        )
        .ExpectGeneratedSource(
            "Test.Target.1.g.cs",
            """
            // <auto-generated>
            //   This code file was generated by the 'Mixer' NuGet package.
            //   See https://github.com/sharpjs/Mixer for more information.
            // </auto-generated>

            namespace Test;

            #region Source
            #nullable enable

            partial $target Target
                : global::Test.IHasThing
            {
                [global::System.CodeDom.Compiler.GeneratedCodeAttribute("Mixer.Generator", "0.0.0.0")]
                global::Test.Thing global::Test.IHasThing.this[int index] => new();
            }

            #nullable restore
            #endregion
            """
        )
        .Test();
    }
}
