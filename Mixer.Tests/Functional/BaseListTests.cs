namespace Mixer.Tests.Functional;

[TestFixture]
public class BaseListTests
{
    [Test]
    public void Simple()
    {
        new FunctionalTestBuilder().WithInput(
            """
            using Mixer;

            namespace Test;

            interface IFoo { }
            interface IBar { }

            [Mixin]
            $source Source : IFoo, IBar { }

            [Include<Source>]
            partial $target Target { }
            """
        )
        .ExpectGeneratedSource(
            "Test.Target.1.g.cs",
            """
            // <auto-generated>
            //   This code file was generated by the 'Mixer' NuGet package.
            //   See https://github.com/sharpjs/Mixer for more information.
            // </auto-generated>

            namespace Test;

            #region Source
            #nullable enable

            partial $target Target
                : global::Test.IFoo, global::Test.IBar
            {
            }

            #nullable restore
            #endregion
            """
        )
        .Test();
    }

    [Test]
    public void ClosedGeneric()
    {
        new FunctionalTestBuilder().WithInput(
            """
            using Mixer;

            namespace Test;

            interface IFoo<T0, T1> { }
            class Bar { }

            [Mixin]
            $source Source : IFoo<int, Bar> { }

            [Include<Source>]
            partial $target Target { }
            """
        )
        .ExpectGeneratedSource(
            "Test.Target.1.g.cs",
            """
            // <auto-generated>
            //   This code file was generated by the 'Mixer' NuGet package.
            //   See https://github.com/sharpjs/Mixer for more information.
            // </auto-generated>

            namespace Test;

            #region Source
            #nullable enable

            partial $target Target
                : global::Test.IFoo<int, global::Test.Bar>
            {
            }

            #nullable restore
            #endregion
            """
        )
        .Test();
    }

    [Test]
    public void OpenGeneric()
    {
        new FunctionalTestBuilder().WithInput(
            """
            using Mixer;

            namespace Test;

            interface IFoo<T0, T1> { }
            class Bar { }

            [Mixin]
            $source Source<T0, T1> : IFoo<T0, T1> { }

            [Include<Source<int, Bar>>]
            partial $target Target { }
            """
        )
        .ExpectGeneratedSource(
            // TODO: Should use keyword `int` instead of type name.
            "Test.Target.1.g.cs",
            """
            // <auto-generated>
            //   This code file was generated by the 'Mixer' NuGet package.
            //   See https://github.com/sharpjs/Mixer for more information.
            // </auto-generated>

            namespace Test;

            #region Source
            #nullable enable

            partial $target Target
                : global::Test.IFoo<global::System.Int32, global::Test.Bar>
            {
            }

            #nullable restore
            #endregion
            """
        )
        .Test();
    }
}
